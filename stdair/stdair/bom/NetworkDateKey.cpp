#include <stdair/bom/NetworkDateKey.hpp>

namespace stdair {

  // ////////////////////////////////////////////////////////////////////
  NetworkDateKey_T::NetworkDateKey_T ()
    : _referenceDate (Date_T (1970, 01, 01)) {
  }
  
  // ////////////////////////////////////////////////////////////////////
  NetworkDateKey_T::NetworkDateKey_T (const Date_T& iReferenceDate)
    : _referenceDate (iReferenceDate) {
  }

  // ////////////////////////////////////////////////////////////////////
  NetworkDateKey_T::NetworkDateKey_T (const NetworkDateKey_T& iKey)
    : _referenceDate (iKey._referenceDate) {
  }

  // ////////////////////////////////////////////////////////////////////
  NetworkDateKey_T::~NetworkDateKey_T () {
  }

  // ////////////////////////////////////////////////////////////////////
  void NetworkDateKey_T::toStream (std::ostream& ioOut) const {
    ioOut << "NetworkDateKey: " << toString() << std::endl;
  }

  // ////////////////////////////////////////////////////////////////////
  void NetworkDateKey_T::fromStream (std::istream& ioIn) {
  }

  // ////////////////////////////////////////////////////////////////////
  const std::string NetworkDateKey_T::toString() const {
    std::ostringstream oStr;
    oStr << _referenceDate;
    return oStr.str();
  }
  
  // ////////////////////////////////////////////////////////////////////
  const std::string NetworkDateKey_T::describe() const {
    std::ostringstream oStr;
    oStr << _parentKey.describe() << ", " << toString();
    return oStr.str();
  }

}
