#include <stdair/bom/NetworkKey.hpp>

namespace stdair {

  // ////////////////////////////////////////////////////////////////////
  NetworkKey_T::NetworkKey_T (const NetworkID_T& iNetworkID)
    : _networkID (iNetworkID) {
  }
  // ////////////////////////////////////////////////////////////////////
  NetworkKey_T::NetworkKey_T (const NetworkKey_T& iKey)
    : _networkID (iKey._networkID) {
  }

  // ////////////////////////////////////////////////////////////////////
  NetworkKey_T::~NetworkKey_T () {
  }

  // ////////////////////////////////////////////////////////////////////
  void NetworkKey_T::toStream (std::ostream& ioOut) const {
    ioOut << "NetworkKey: " << toString() << std::endl;
  }

  // ////////////////////////////////////////////////////////////////////
  void NetworkKey_T::fromStream (std::istream& ioIn) {
  }

  // ////////////////////////////////////////////////////////////////////
  const std::string NetworkKey_T::toString() const {
    std::ostringstream oStr;
    oStr << _networkID;
    return oStr.str();
  }

}
